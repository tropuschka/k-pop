function Search() {
	let got = document.getElementById('search').value;
	let searched = got.toUpperCase();
	let placement = document.getElementById('place');
	let found = [];

	for (let elem in groupList) {
		let searchWordList = groupList[elem].search;
		let incl = 0;

		for (let searchW in searchWordList) {
			if (searchWordList[searchW] == searched || searchWordList[searchW].includes(searched)) incl = 1;
		}

		if (incl == 1) {
			found.push(elem);
		}
	}

	if (found.length == 0) {
		placement.innerHTML = 'Совпадений не найдено, попробуйте изменить запрос или поискать на <a href="https://kpop.fandom.com/ru/wiki/K-pop_вики">K-pop Wiki</a>';
	} else if (found.length == 1 && found[0] == searched) {
		Choose(searched);
	} else {
		let foundS = found.sort();

		let resStr = '<h2>Возможно, вы искали:</h2>';

		for (let name in foundS) {
			let nameForList = foundS[name];
			resStr = resStr + `<button class='found-button' type='button' onClick='Choose("${foundS[name]}")'>${foundS[name]}</button> - <a href='${groupList[nameForList].link}' target='_blank'>Открыть в новой вкладке</a><br>`;
		}
		placement.innerHTML = resStr;	
	}	
}


